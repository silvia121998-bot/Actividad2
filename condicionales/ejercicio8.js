// EJERCICIO 8: CUENTA REGRESIVA
// Mostrar números del 10 al 1
// ==========================================

// FOR
for (let i = 10; i >= 1; i--) {
  console.log("FOR:", i);
}

// WHILE
let i1 = 10;

while (i1 >= 1) {
  console.log("WHILE:", i1);
  i1--;
}

// DO-WHILE
let i2 = 10;

do {
  console.log("DO-WHILE:", i2);
  i2--;
} while (i2 >= 1);